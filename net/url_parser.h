    /* URL storage */
    typedef struct {
        char *scheme; /* mandatory */
        char *host; /* mandatory */
        char *port; /* optional */
        char *path; /* optional */
        char *query; /* optional */
        char *fragment; /* optional */
        char *username; /* optional */
        char *password; /* optional */
    }t_URL_DATA;

    /* ************************************************************************** */
    /* ************************************************************************** */
    /* Section: Interface Functions                                               */
    /* ************************************************************************** */
    /* ************************************************************************** */
